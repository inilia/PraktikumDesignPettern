package modul_1;

public class Percabangan {
    public static void main(String[] args) {
        int angka = 10;

        if (angka > 0) {
            System.out.println("Angka adalah positif");
        } else if (angka < 0) {
            System.out.println("Angka adalah negatif");
        } else {
            System.out.println("Angka adalah nol");
        }
    }
}