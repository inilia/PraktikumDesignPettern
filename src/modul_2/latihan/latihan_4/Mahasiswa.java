package modul_2.latihan.latihan_4;

public class Mahasiswa {
    private String nama;
    private String nim;

    // Setter untuk nama
    public void setNama(String nama){
        this.nama = nama;
    }

    // Getter untuk nama
    public String getNama() {
        return nama;
    }

    // Setter utuk NIM
    public void setNim(String nim) {
        this.nim = nim;
    }

    // Getter untuk NIM
    public String getNim() {
        return nim;
    }
}
